package org.topaz.cpu

class MemoryManager{
    private static final int MEMORY_SIZE = 0x10000
    private int []rom

    public MemoryManager() {
        this.rom = new int[MEMORY_SIZE]
        this.init()
    }

    private void init() {
        rom[0xFF05] = 0x00
        rom[0xFF06] = 0x00
        rom[0xFF07] = 0x00
        rom[0xFF10] = 0x80
        rom[0xFF11] = 0xBF
        rom[0xFF12] = 0xF3
        rom[0xFF14] = 0xBF
        rom[0xFF16] = 0x3F
        rom[0xFF17] = 0x00
        rom[0xFF19] = 0xBF
        rom[0xFF1A] = 0x7F
        rom[0xFF1B] = 0xFF
        rom[0xFF1C] = 0x9F
        rom[0xFF1E] = 0xBF
        rom[0xFF20] = 0xFF
        rom[0xFF21] = 0x00
        rom[0xFF22] = 0x00
        rom[0xFF23] = 0xBF
        rom[0xFF24] = 0x77
        rom[0xFF25] = 0xF3
        rom[0xFF26] = 0xF1
        rom[0xFF40] = 0x91
        rom[0xFF42] = 0x00
        rom[0xFF43] = 0x00
        rom[0xFF45] = 0x00
        rom[0xFF47] = 0xFC
        rom[0xFF48] = 0xFF
        rom[0xFF49] = 0xFF
        rom[0xFF4A] = 0x00
        rom[0xFF4B] = 0x00
        rom[0xFFFF] = 0x00
    }
}